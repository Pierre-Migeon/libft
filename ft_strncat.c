#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	while (*s1)
		s1++;
	while (s2[i] && i < n)
	{
		*s1 = s2[i++];
		s1++;
	}
	*s1 = '\0';
	return (s1);
}
